n = int(input('Digite um número: '))
if n % 2 == 0:
    print('Esse número é PAR!')
else:
    print('Esse número é ÍMPAR! ')

# Para descobrir se o número digitado pelo usuário é PAR ou IMPAR
## peguei o número digitado e fiz a condição se(if) o resto da divisão dele por 2 for 0 então ele é PAR senão(else) IMPAR. 